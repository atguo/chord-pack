const shuffleArray = <T>(array: T[]) => {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
};

const data = shuffleArray(internalData);

const circleNames: string[] = [];
const chordNames: string[] = [];


const INTRANET_ADDR_PREFIX: string[] = ["10.", "192.168"];
const METRIC = "totalBytes";


for (const item of data) {
  const ipAIsIntrannet = !!INTRANET_ADDR_PREFIX.find((prefix) =>
    item.ipAAddress.startsWith(prefix)
  );
  const ipBIsIntrannet = !!INTRANET_ADDR_PREFIX.find((prefix) =>
    item.ipBAddress.startsWith(prefix)
  );

  if (
    (ipAIsIntrannet && !ipBIsIntrannet) ||
    (ipBIsIntrannet && !ipAIsIntrannet)
  ) {
    const intranet = ipAIsIntrannet ? item.ipAAddress : item.ipBAddress;
    const internet = ipAIsIntrannet ? item.ipBAddress : item.ipAAddress;
    if (!circleNames.includes(intranet)) {
      circleNames.push(intranet);
    }
    if (!chordNames.includes(internet)) {
      chordNames.push(internet);
    }
  }
}


const dataMatrix: number[][] = new Array(chordNames.length)
  .fill(null)
  .map(() => new Array(circleNames.length).fill(0));

for (const item of data) {
  const ipAIsIntrannet = !!INTRANET_ADDR_PREFIX.find((prefix) =>
    item.ipAAddress.startsWith(prefix)
  );
  const ipBIsIntrannet = !!INTRANET_ADDR_PREFIX.find((prefix) =>
    item.ipBAddress.startsWith(prefix)
  );

  if (
    (ipAIsIntrannet && !ipBIsIntrannet) ||
    (ipBIsIntrannet && !ipAIsIntrannet)
  ) {
    const intranet = ipAIsIntrannet ? item.ipAAddress : item.ipBAddress;
    const internet = ipAIsIntrannet ? item.ipBAddress : item.ipAAddress;

    const rowIndex = chordNames.findIndex((chord) => chord === internet);
    const colIndex = circleNames.findIndex((circle) => circle === intranet);

    if (rowIndex >= 0) {
      dataMatrix[rowIndex][colIndex] += item[METRIC];
    }
  }
}

